#include <stdio.h>

#include "./ex02/ft_abs.h"

int	ABS(int value)
{
	if (value < 0)
	{
		return (-value);
	}
	else
	{
		return (value);
	}
}

int	main(void)
{
	int	index;

	index = -5;
	while (index < 5)
	{
		printf("macros::abs(%d) = %d\n", index, ABS(index));
		index++;
	}
}
